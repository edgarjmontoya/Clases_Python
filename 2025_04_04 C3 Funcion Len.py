# Cadenas de texto
texto = "Hola, mundo"
print("Longitud de la cadena:", len(texto))

# Listas
lista = [1, 2, 3, 4, 5]
print("Longitud de la lista:", len(lista))

# Tuplas
tupla = (1, 2, 3)
print("Longitud de la tupla:", len(tupla))

# Diccionarios
diccionario = {"a": 1, "b": 2, "c": 3}
print("Longitud del diccionario:", len(diccionario))

# Conjuntos
conjunto = {1, 2, 3, 4, 5}
print("Longitud del conjunto:", len(conjunto))